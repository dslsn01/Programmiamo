/* file Cubes.java */

public class Cubes {
	public static void main(String [] args) {
		/* dichiara tre variabili */

		int n1 = 3;
		int n2 = 4;
		int n3 = 5;

		/* calcola i cubi */

		int cube1 = n1 * n1 * n1;
		int cube2 = n2 * n2 * n2;
		int cube3 = n3 * n3 * n3;

		/* stampa i cubi */

		System.out.println(n1 + " al cubo = " + cube1);
		System.out.println(n2 + " al cubo = " + cube2);
		System.out.println(n3 + " al cubo = " + cube3);
	}
}

