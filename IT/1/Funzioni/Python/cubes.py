cube1 = n1 * n1 * n1 
cube2 = n2 * n2 * n2 
cube3 = n3 * n3 * n3

# stampa i cubi

print("{} al cubo = {}".format(n1, cube1))
print("{} al cubo = {}".format(n2, cube2))
print("{} al cubo = {}".format(n3, cube3))

